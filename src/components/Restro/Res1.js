import React, { useState, useEffect } from 'react';
import { motion } from 'framer-motion';
import { BsCart3 } from 'react-icons/bs';
import { Link } from 'react-router-dom';

const Res1 = () => {
  const savedCart = localStorage.getItem('cart1'); // Use a different key for Res2
  const [cart, setCart] = useState(savedCart ? JSON.parse(savedCart) : []);
  const [warning, setWarning] = useState(false);

  const addToCart = (item) => {
    let isPresent = false;
    cart.forEach((product) => {
      if (item.id === product.id) isPresent = true;
    });
    if (isPresent) {
      setWarning(true);
      setTimeout(() => {
        setWarning(false);
      }, 1000);
      return;
    }
    setCart([...cart, item]);
  };

  useEffect(() => {
    localStorage.setItem('cart1', JSON.stringify(cart)); // Use 'cart2' as the key
  }, [cart]);

  const items = [
    { id: 1, name: 'Item 1 - ', price: '$10' },
    { id: 2, name: 'Item 2 - ', price: '$15' },
    { id: 3, name: 'Item 3 - ', price: '$20' },
    { id: 4, name: 'Item 4 - ', price: '$12' },
    { id: 5, name: 'Item 5 - ', price: '$18' },
    { id: 6, name: 'Item 6 - ', price: '$25' },
    { id: 7, name: 'Item 7 - ', price: '$22' },
    { id: 8, name: 'Item 8 - ', price: '$16' },
    { id: 9, name: 'Item 9 - ', price: '$30' },
  ];

  return (
    <div className="bg-blue-500 w-screen min-h-screen flex flex-col items-center justify-start p-8">
      <motion.button
        variants={{
          hidden: { x: 60, opacity: 0 },
          show: { x: 0, opacity: 1 },
        }}
        initial="hidden"
        animate="show"
        transition={{ duration: 0.7 }}
        className="font-medium text-xl ml-auto bg-blue-300 rounded-full px-6 py-2"
      >
        <Link to="./Carts/Cart1" className="flex flex-row items-center">
          <BsCart3 />
          {cart.length > 0 && <span className="ml-2">{cart.length}</span>}
        </Link>
      </motion.button>
      <div className="font-medium text-xl ml-auto rounded-full px-6 py-4">
        {warning && <div className="text-red-800">Item is already added to your cart</div>}
      </div>

      {items.map((item) => (
        <motion.div
          variants={{
            hidden: { x: 60, opacity: 0 },
            show: { x: 0, opacity: 1 },
          }}
          initial="hidden"
          animate="show"
          transition={{ duration: 0.7 }}
          key={item.id}
          className="bg-blue-300 rounded-lg p-4 mb-4 w-full max-w-md flex items-center justify-between"
        >
          <div className="flex">
            <p className="mx-4">{item.name}</p>
            <p className="text-gray-600 text-sm">{item.price}</p>
          </div>
          <button
            onClick={() => addToCart(item)}
            className="bg-blue-600 text-white rounded-full hover:bg-blue-900 hover:text-gray-200 px-2 py-1"
          >
            Add to cart
          </button>
        </motion.div>
      ))}
    </div>
  );
};

export default Res1;





























































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































          